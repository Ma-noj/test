
public class ExampleOfVaribles8 {
	static byte num1;//
	static short num2;
	static int num3;
	static long num4;
	static float decimal1;
	static double decimal2;
	static boolean flag;
	static char charcter;
	static String str;

	public static void main(String[] args) {
		System.out.println("byte  num1 = " + num1);
		System.out.println("short  num2 = " + num2);
		System.out.println("int  num3 = " + num3);
		System.out.println("long  num4 = " + num4);
		System.out.println("float  decimal1 = " + decimal1);
		System.out.println("double  decimal2 = " + decimal2);
		System.out.println("boolean  flag = " + flag);
		System.out.println("char  charcter = " + charcter);
		System.out.println("String  str = " + str);

	}

}
