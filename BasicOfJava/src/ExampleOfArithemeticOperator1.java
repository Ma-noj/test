
public class ExampleOfArithemeticOperator1 {
	public static void main(String[] args) {
		System.out.println(10 + 20 + " Add");// 10+20=30 => "30 Add"
		System.out.println("Add " + 10 + 20);// "Add 10"+20==>"Add 1020"
		System.out.println("Hello " + "World");
		System.out.println("A"+20);
		System.out.println(1+2+"A"+20);
		System.out.println('A'+20);// A->65
		System.out.println('a'+10);// a->97
		System.out.println(1+'Z');// Z->90
		System.out.println(10+'A'+" Hello");
		System.out.println(" Hello "+'A'+10);
	}
}
