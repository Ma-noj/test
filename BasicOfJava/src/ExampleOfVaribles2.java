
public class ExampleOfVaribles2 {
	public static void main(String[] args) {
		// Numerical Type
		byte num;// -128 to 127
		num = 127;
		System.out.println(num);

		short num1;
		num1 = 32767;
		System.out.println(num1);

		int num2;
		num2 = 32769;
		System.out.println(num2);

		long num3;
		num3 = 92837635463l;
		System.out.println(num3);

		float num4;
		num4 = 3.1f;
		System.out.println(num4);

		double num5;
		num5 = 2.1;
		System.out.println(num5);

		boolean flag;
		flag = true;
		System.out.println(flag);

		char character;
		character = 'z';
		System.out.println(character);

		String str;
		str = "Hi Hello Welcome 1234";
		System.out.println(str);

		int num6;
		num6 = 10;
		int num7;
		num7 = 20;
		int num8 = num6 + num7;
		System.out.println(num8);

	}
}
