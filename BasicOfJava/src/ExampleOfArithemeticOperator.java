
public class ExampleOfArithemeticOperator {
	public static void main(String[] args) {
		int num1 = 25;
		int num2 = 12;
		System.out.println(num1+num2);
		System.out.println(num1-num2);
		System.out.println(num1*num2);
		System.out.println(num1/num2);//quotient Value
		System.out.println(num1%num2);
	}
}
